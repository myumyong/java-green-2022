package ex05;

public class LogicalOperator {
    public static void main(String[] args) {
        int x = 3;
        int y = 4;

        // 논리연산자
        // && 그리고 (and)
        // || 거나 (and)
        System.out.println((x == 3) && (y == 7)); // true && false = false
        System.out.println((x == 3) || (y == 4)); // true || false = true
    }

}
